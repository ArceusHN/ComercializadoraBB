    public void InicializartbProductos(ArrayList<ProductoModelo> listado){
        DefaultTableModel tabla =  new DefaultTableModel();
        String[] fila = new String[6];
        tabla.addColumn("CÃ³digo");
        tabla.addColumn("Nombre");
        tabla.addColumn("Tipo Producto");
        tabla.addColumn("Categoria");
        tabla.addColumn("Inventario Global");
        tabla.addColumn("Fecha Agregado");        
        for(int f=0; f<listado.size();f++){
            fila[0] = String.valueOf(listado.get(f).getId());
            fila[1] = listado.get(f).getDescripcion();
            fila[2] = String.valueOf(listado.get(f).getTipoProductoDescripcion());
            fila[3] = String.valueOf(listado.get(f).getCategoria());
            fila[4] = String.valueOf(listado.get(f).getStockGlobal());
            fila[5] = listado.get(f).getFechaAgrega().toString();
            tabla.addRow(fila);
        }
        tbProductos.setModel(tabla);
    }
